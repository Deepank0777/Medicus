    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container, Bundle savedInstanceState) {
        View view = inflater.inflate(R.layout.fragment_general_doc_info, null);
        Bundle bundle = getActivity().getIntent().getExtras();
        String professionalStatement = bundle.getString("professionalStatement");
        String qualificationName = bundle.getString("professionalStatement");
        String instituteName = bundle.getString("instituteName");
        String procurementYear = bundle.getString("procurementYear");

        statmentInfo = view.findViewById(R.id.statementInfo);
        qualificationInfo = view.findViewById(R.id.qualificationInfo);
        instituteInfo = view.findViewById(R.id.instituteInfo);
        procurementInfo = view.findViewById(R.id.procurementInfo);

        statmentInfo.setText(professionalStatement);
        qualificationInfo.setText(qualificationName);
        instituteInfo.setText(instituteName);
        procurementInfo.setText(procurementYear);
        return view;
    }
